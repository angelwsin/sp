

package com.db.bean;

public class Menu {

  
   
private   int   id;
   
private   String   type;
   
private   String   name;
   
private   String   key;
   
private   String   url;
   
private   int   pId;
   
public int getId(){
       return this.id;
    }
public void setId(int id){
    this.id=id;
   }
public String getType(){
       return this.type;
    }
public void setType(String type){
    this.type=type;
   }
public String getName(){
       return this.name;
    }
public void setName(String name){
    this.name=name;
   }
public String getKey(){
       return this.key;
    }
public void setKey(String key){
    this.key=key;
   }
public String getUrl(){
       return this.url;
    }
public void setUrl(String url){
    this.url=url;
   }
public int getPId(){
       return this.pId;
    }
public void setPId(int pId){
    this.pId=pId;
   }

}